/**
 * @param {object} root0 - Params
 * @param {boolean} root0.withText - Whether the OpenNebula text is displayed
 * @param {number} root0.width - Logo width
 * @param {number} root0.height - Logo height
 * @param {string} root0.color - Logo color
 * @returns {Component} - OpenNebula icon component.
 */
export const OpenNebulaIcon = ({ withText = false, width, height, color }) => {
  const { settings: { SCHEME = SCHEMES.SYSTEM } = {} } = useAuth()
  const prefersDarkMode = useMediaQuery('(prefers-color-scheme: dark)')
  const isDarkMode =
    SCHEME === SCHEMES.SYSTEM ? prefersDarkMode : SCHEME === SCHEMES.DARK

  const iconColor = color ?? (isDarkMode ? '#40B3D9' : '#007099')
  const dimensions = withText ? logoDimensions.open : logoDimensions.collapsed
  const Logo = withText ? OpenLogo : ClosedLogo

  return (
    <Logo
      width={width ?? dimensions.width}
      height={height ?? dimensions.height}
      color={iconColor}
    />
  )
}

OpenNebulaIcon.propTypes = {
  withText: PropTypes.bool,
  width: PropTypes.number,
  height: PropTypes.number,
  color: PropTypes.string,
}

OpenNebulaIcon.displayName = 'OpenNebulaIcon'
